// Initialize Web3
let web3;

// Initialize the smart contract instance
let contractInstance;

// Function to initialize Web3 and the smart contract
async function initWeb3() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        try {
            // Request account access if needed
            await window.ethereum.enable();
            // Instantiate contract
            contractInstance = new web3.eth.Contract(contractABI, contractAddress);
            console.log('Web3 initialized with contract');
            fetchNFTs();
        } catch (error) {
            console.error('User denied account access:', error);
        }
    } else {
        console.error('Web3 provider not detected');
    }
}

// Fetch NFT data from backend API
async function fetchNFTs() {
    try {
        const response = await fetch('/api/nfts');
        if (!response.ok) {
            throw new Error('Network response was not ok');
        }
        const data = await response.json();
        displayNFTs(data);
    } catch (error) {
        console.error('Error fetching NFTs:', error);
    }
}

// Display NFTs in the HTML
function displayNFTs(nfts) {
    const nftListElement = document.getElementById('nftList');

    nfts.forEach(nft => {
        const nftCard = document.createElement('div');
        nftCard.classList.add('nft-card');

        const nftImage = document.createElement('img');
        nftImage.src = nft.image;
        nftImage.alt = nft.title;

        const nftDetails = document.createElement('div');
        nftDetails.classList.add('nft-details');

        const nftTitle = document.createElement('h3');
        nftTitle.classList.add('nft-title');
        nftTitle.textContent = nft.title;

        const nftDescription = document.createElement('p');
        nftDescription.classList.add('nft-description');
        nftDescription.textContent = nft.description;

        const buyButton = document.createElement('button');
        buyButton.classList.add('buy-btn');
        buyButton.textContent = 'Buy';
        buyButton.addEventListener('click', () => buyNFT(nft.id));

        nftDetails.appendChild(nftTitle);
        nftDetails.appendChild(nftDescription);

        nftCard.appendChild(nftImage);
        nftCard.appendChild(nftDetails);
        nftCard.appendChild(buyButton);

        nftListElement.appendChild(nftCard);
    });
}

// Function to handle buying an NFT
async function buyNFT(nftId) {
    try {
        // Request account access if needed
        await window.ethereum.enable();
        // Get the current account
        const accounts = await web3.eth.getAccounts();
        const account = accounts[0];
        // Call the mint function on the smart contract
        await contractInstance.methods.mint(account).send({
            from: account
        });
        alert('NFT bought successfully!');
    } catch (error) {
        console.error('Error buying NFT:', error);
        alert('Error buying NFT. See console for details.');
    }
}

// Initialize Web3 and the smart contract when the page loads
window.addEventListener('DOMContentLoaded', () => {
    initWeb3();
});
